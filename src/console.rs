/// The calling process's console window title — `GetConsoleTitleW`, the
/// Windows analog of xterm's OSC title-setting escape sequence's read-back
/// side. Reports an empty string, not an error, for a console with no title
/// set (`GetConsoleTitleW`'s own documented quirk: a zero return means
/// either "buffer too small" or "empty title," distinguished only by
/// `GetLastError` reporting success in the empty-title case — the same
/// quirk [`crate::process::get_env_var`] handles for
/// `GetEnvironmentVariableW`).
pub fn title() -> Result<alloc::string::String, Win32Error> {
    let mut buf: alloc::vec::Vec<u16> = alloc::vec![0u16; 1024];
    // At most two attempts: an initial try, then one retry sized exactly to
    // whatever `GetConsoleTitleW` reports as actually required (a returned
    // length `>=` the buffer size means it didn't fit) — matching
    // `path::current_dir`'s own growing-buffer pattern.
    for _ in 0..2 {
        // SAFETY: `buf` is a valid, `buf.len()`-element writable buffer.
        let len = unsafe { GetConsoleTitleW(buf.as_mut_ptr(), buf.len() as u32) };
        if len == 0 {
            return match Win32Error::last() {
                Win32Error::SUCCESS => Ok(alloc::string::String::new()),
                err => Err(err),
            };
        }
        if (len as usize) >= buf.len() {
            buf.resize(len as usize + 1, 0);
            continue;
        }
        return Ok(alloc::string::String::from_utf16_lossy(
            &buf[..len as usize],
        ));
    }
    Err(Win32Error::ERROR_INSUFFICIENT_BUFFER)
}

/// Set the calling process's console window title — `SetConsoleTitleW`, the
/// Windows analog of xterm's OSC title-setting escape sequence. A common
/// shell feature (showing the current working directory or running command
/// in the window title) that this crate previously had no primitive for at
/// all.
pub fn set_title(title: &str) -> Result<(), Win32Error> {
    let wide: alloc::vec::Vec<u16> = title.encode_utf16().chain(core::iter::once(0)).collect();
    // SAFETY: `wide` is a valid, NUL-terminated UTF-16 string.
    let ok = unsafe { SetConsoleTitleW(wide.as_ptr()) };
    if ok == 0 {
        Err(Win32Error::last())
    } else {
        Ok(())
    }
}

/// Read up to `buf.len()` bytes from `handle` in one call — `ReadFile`, the
/// Windows analog of a raw Unix `read`. With `ENABLE_VIRTUAL_TERMINAL_INPUT`
/// set on an input handle, this delivers a VT/ANSI byte stream a Unix-style
/// key decoder can parse largely unchanged; without it, this reads raw
/// `INPUT_RECORD` bytes, which is almost certainly not what a caller wants
/// (use `ReadConsoleInputW` — out of this crate's current scope — for
/// structured input records instead).
///
/// # Safety
///
/// `handle` must be a currently-open, valid handle open for reading.
pub unsafe fn read(handle: RawHandle, buf: &mut [u8]) -> Result<usize, Win32Error> {
    let mut bytes_read: u32 = 0;
    // SAFETY: `handle` is caller-supplied per this function's own safety
    // contract; `buf` is a valid, `buf.len()`-byte writable buffer;
    // `bytes_read` is a valid out-pointer; `lpOverlapped = NULL` requests a
    // synchronous (blocking) read, a documented valid input.
    let ok = unsafe {
        ReadFile(
            handle,
            buf.as_mut_ptr(),
            buf.len() as u32,
            &mut bytes_read,
            core::ptr::null_mut(),
        )
    };
    if ok == 0 {
        Err(Win32Error::last())
    } else {
        Ok(bytes_read as usize)
    }
}

/// Block on `handle` for up to `timeout_ms` — `WaitForSingleObject`, the
/// Windows analog of `poll` on a single fd. Returns `true` if the handle
/// became signaled (input is available, for a console input handle) within
/// the timeout, `false` on timeout.
///
/// # Safety
///
/// `handle` must be a currently-open, valid, waitable handle.
pub unsafe fn wait_readable(handle: RawHandle, timeout_ms: u32) -> Result<bool, Win32Error> {
    // SAFETY: `handle` is caller-supplied per this function's own safety
    // contract.
    let result = unsafe { WaitForSingleObject(handle, timeout_ms) };
    match result {
        WAIT_OBJECT_0 => Ok(true),
        WAIT_TIMEOUT => Ok(false),
        _ => Err(Win32Error::last()),
    }
}

/// The visible console window size, as `(columns, rows)` —
/// `GetConsoleScreenBufferInfo`, the Windows analog of `TIOCGWINSZ`.
/// Reports the *visible viewport* (`srWindow`), not the full scrollback
/// buffer (`dwSize`, which can be much taller) — the same quantity
/// `ws_col`/`ws_row` report on Unix.
///
/// # Safety
///
/// `console_output` must be a currently-open, valid handle to a console
/// output buffer.
pub unsafe fn window_size(console_output: RawHandle) -> Result<(u16, u16), Win32Error> {
    let mut info = ConsoleScreenBufferInfo::default();
    // SAFETY: `console_output` is caller-supplied per this function's own
    // safety contract; `info` is a valid, correctly-sized out-pointer.
    let ok = unsafe { GetConsoleScreenBufferInfo(console_output, &mut info) };
    if ok == 0 {
        return Err(Win32Error::last());
    }
    let cols = (info.window.right - info.window.left + 1).max(0) as u16;
    let rows = (info.window.bottom - info.window.top + 1).max(0) as u16;
    Ok((cols, rows))
}

/// The largest the console window could be resized to, in character
/// cells, given the current font and the display it's on —
/// `GetLargestConsoleWindowSize`. The write-side counterpart to
/// [`window_size`]'s read-only report: a caller planning a
/// [`set_screen_buffer_size`]/[`set_window_info`] resize needs this as the
/// upper bound, since requesting a window larger than this fails.
/// Documented as reporting `(0, 0)` on failure rather than a separate
/// error path — matching this crate's already-established "never fails in
/// practice" pattern (e.g. `GetDriveTypeW`) since a real console can't
/// legitimately have a zero-cell maximum.
///
