// Console input-mode bits (`GetConsoleMode`/`SetConsoleMode` on an input
// handle). What "raw mode" means in terms of these — which bits to clear
// or set — is deliberately *not* decided here, the same way `rusty_libc`
// exposes `tcgetattr`/`tcsetattr` without baking in a raw-mode recipe:
// that policy belongs to the line-editor consumer (`rusty_lines`'
// `term_sys.rs` on Unix), not this crate.
/// Ctrl+C/Ctrl-Break are intercepted and converted to the corresponding
/// `CTRL_*_EVENT` (via [`install_ctrl_handler`]) rather than delivered as
/// input — set by default. Clearing this is what lets a raw-mode reader see
/// Ctrl+C as ordinary input instead of the OS terminating the process.
pub const ENABLE_PROCESSED_INPUT: u32 = 0x0001;
/// Line-buffered input: `ReadFile`/`ReadConsole` doesn't return until a
/// carriage return, and backspace/editing happens in the console host
/// itself — set by default. The Windows analog of `ICANON`; clear it for
/// the same reason a Unix raw-mode setup clears `ICANON`.
pub const ENABLE_LINE_INPUT: u32 = 0x0002;
/// The console host echoes typed characters — set by default, only
/// meaningful with `ENABLE_LINE_INPUT` also set. The Windows analog of
/// `ECHO`.
pub const ENABLE_ECHO_INPUT: u32 = 0x0004;
/// `WINDOW_BUFFER_SIZE_EVENT` input records are generated on resize.
pub const ENABLE_WINDOW_INPUT: u32 = 0x0008;
/// Mouse events are reported as input records.
pub const ENABLE_MOUSE_INPUT: u32 = 0x0010;
pub const ENABLE_INSERT_MODE: u32 = 0x0020;
/// Mouse/keyboard input is consumed by the console's own Quick Edit
/// selection UI rather than delivered to the application — usually wanted
/// off for a raw-mode reader for the same reason it's wanted off for any
/// full-screen or line-editing console app.
pub const ENABLE_QUICK_EDIT_MODE: u32 = 0x0040;
/// Must be set for `ENABLE_INSERT_MODE`/`ENABLE_QUICK_EDIT_MODE` to have
/// any effect — a documented Windows quirk, not a real feature of its own.
pub const ENABLE_EXTENDED_FLAGS: u32 = 0x0080;
/// `ReadFile`/`ReadConsole` on this input handle delivers a VT/ANSI escape
/// byte stream for special keys (arrows, function keys, …) instead of
/// requiring `ReadConsoleInputW`'s structured `INPUT_RECORD`s — the single
/// bit that makes a Windows 10+ console behave like a Unix tty for input
/// purposes, and the reason this crate's Windows floor (10 1809+) is new
/// enough to support it at all.
pub const ENABLE_VIRTUAL_TERMINAL_INPUT: u32 = 0x0200;

// Console output-mode bits (`GetConsoleMode`/`SetConsoleMode` on an output
// handle).
pub const ENABLE_PROCESSED_OUTPUT: u32 = 0x0001;
pub const ENABLE_WRAP_AT_EOL_OUTPUT: u32 = 0x0002;
/// VT/ANSI escape sequences written to this output handle are interpreted
/// (cursor movement, colors, …) instead of printed literally — the output
/// counterpart of `ENABLE_VIRTUAL_TERMINAL_INPUT`, needed for the same
/// repaint escape sequences a Unix terminal already understands by default.
pub const ENABLE_VIRTUAL_TERMINAL_PROCESSING: u32 = 0x0004;
/// A bare `\n` moves down without returning to column 0 — matching how a
/// Unix terminal's `\n` behaves once `ONLCR`-style translation is off,
/// needed so a VT-aware repaint (which sends its own `\r` where it means
/// it) doesn't get an extra implicit carriage return.
pub const DISABLE_NEWLINE_AUTO_RETURN: u32 = 0x0008;

