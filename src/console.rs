pub fn set_input_codepage(code_page_id: u32) -> Result<(), Win32Error> {
    // SAFETY: `code_page_id` is a plain value, not a pointer.
    let ok = unsafe { SetConsoleCP(code_page_id) };
    if ok == 0 {
        Err(Win32Error::last())
    } else {
        Ok(())
    }
}

