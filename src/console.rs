/// The pids of every process currently attached to the calling process's
/// console — `GetConsoleProcessList`, e.g. for an "is anything else still
/// attached to this console" check. No current `rush` feature asks for
/// this; filed for Win32 parity.
pub fn process_list() -> Result<alloc::vec::Vec<u32>, Win32Error> {
    let mut buf: alloc::vec::Vec<u32> = alloc::vec![0u32; 8];
    // `GetConsoleProcessList` reports how many pids it actually needed
    // (which can be larger than the buffer just tried) in its own return
    // value — grow to that exact size and retry, the same shape this
    // crate's other "ask, then maybe grow" calls use, just via a return
    // value instead of an `ERROR_INSUFFICIENT_BUFFER`/ in-out size
    // parameter.
    for _ in 0..2 {
        // SAFETY: `buf` is a valid, `buf.len()`-element writable buffer
        // matched by the `process_count` argument naming its exact
        // length.
        let needed = unsafe { GetConsoleProcessList(buf.as_mut_ptr(), buf.len() as u32) };
        if needed == 0 {
            return Err(Win32Error::last());
        }
        if (needed as usize) > buf.len() {
            buf.resize(needed as usize, 0);
            continue;
        }
        buf.truncate(needed as usize);
        return Ok(buf);
    }
    Err(Win32Error::ERROR_INSUFFICIENT_BUFFER)
}

/// The `HWND` of the console window attached to the calling process, if
/// any — `GetConsoleWindow`. `None` for a process with no console window
/// (e.g. a headless/service process). Manipulating the window itself
/// (position/focus/etc.) via ordinary `user32` window APIs is out of this
/// crate's scope beyond returning the handle. No `Result`: `GetConsoleWindow`
/// has no documented failure mode beyond the `None` case itself, matching
/// this crate's already-established "never fails" pattern (e.g.
/// `GetDriveTypeW`). No current `rush` feature asks for this; filed for
/// Win32 parity.
pub fn window_handle() -> Option<*mut core::ffi::c_void> {
    // SAFETY: `GetConsoleWindow` takes no arguments and has no
    // precondition.
    let hwnd = unsafe { GetConsoleWindow() };
    if hwnd.is_null() { None } else { Some(hwnd) }
}

/// `AttachConsole`'s `dwProcessId` sentinel: attach to the console of the
/// calling process's own parent, rather than a specific pid.
pub const ATTACH_PARENT_PROCESS: u32 = 0xFFFF_FFFF;

/// Allocate a brand-new console for the calling process — `AllocConsole`.
/// Fails with `ERROR_ACCESS_DENIED` if the process already has a console.
/// Needed by a GUI-subsystem process that wants to acquire a console on
/// demand; this crate's own tests already use `AllocConsole` internally
/// (see `ensure_console_stdin` below) but hadn't exposed it as public API
/// until now. No current `rush` feature asks for this; filed for Win32
/// parity.
pub fn alloc() -> Result<(), Win32Error> {
    // SAFETY: `AllocConsole` takes no arguments and has no precondition.
    if unsafe { AllocConsole() } == 0 {
        return Err(Win32Error::last());
    }
    Ok(())
}

/// Detach the calling process from its console — `FreeConsole`. A
/// GUI-subsystem process that acquired a console via [`alloc`] (or
/// inherited one) can use this to give it up again, e.g. before calling
/// [`alloc`] a second time to create a fresh one.
pub fn free() -> Result<(), Win32Error> {
    // SAFETY: `FreeConsole` takes no arguments and has no precondition.
    if unsafe { FreeConsole() } == 0 {
        return Err(Win32Error::last());
    }
    Ok(())
}

/// Attach the calling process to the console of another process —
/// `AttachConsole`. `pid = None` maps to `ATTACH_PARENT_PROCESS`, attaching
/// to whatever console (if any) launched this process — the usual case
/// for a GUI-subsystem process that wants to reuse a console its parent
/// already has, rather than allocate its own via [`alloc`]. The calling
/// process must not already have a console (see [`free`]).
pub fn attach(pid: Option<u32>) -> Result<(), Win32Error> {
    let process_id = pid.unwrap_or(ATTACH_PARENT_PROCESS);
    // SAFETY: `AttachConsole` takes a plain `DWORD` process id (or the
    // documented `ATTACH_PARENT_PROCESS` sentinel) and has no other
    // precondition.
    if unsafe { AttachConsole(process_id) } == 0 {
        return Err(Win32Error::last());
    }
    Ok(())
}

/// Synthesize `text` as a sequence of real console key events —
/// `WriteConsoleInputW`, the standard, documented technique console
/// automation tools use to inject keystrokes (queues `INPUT_RECORD`s into
/// the same input buffer a real keyboard driver would). Each character
/// becomes a key-down/key-up pair carrying it via `uChar`, not a real
/// virtual-key code — valid for ordinary printable characters *and* ASCII
/// control characters (`'\r'`, `\x03` for Ctrl-C, `\x10` for Ctrl-P, …:
/// Windows' own real key events for those carry exactly these `uChar`
/// values), but not for non-character keys (arrows, function keys, Home/
/// End) that carry no `uChar` at all — those need real virtual-key codes,
/// out of this function's scope. Returns the number of events written
/// (twice the character count, barring a partial write).
///
/// With `ENABLE_VIRTUAL_TERMINAL_INPUT` set on `handle` (see
/// [`ENABLE_VIRTUAL_TERMINAL_INPUT`]), a subsequent [`read`] on the same
/// handle observes these as the equivalent VT/ANSI byte(s) — the same
/// translation a real keypress goes through — making this the primitive a
/// test can use to drive a raw-mode reader through its real Windows I/O
/// path end to end, the Windows analog of writing bytes into one end of a
/// Unix pty.
///
/// # Safety
///
/// `handle` must be a currently-open, valid console input handle.
pub unsafe fn write_char_events(handle: RawHandle, text: &str) -> Result<u32, Win32Error> {
    extern crate alloc;
    use alloc::vec::Vec;

    let mut records: Vec<InputRecordKeyEvent> = Vec::with_capacity(text.chars().count() * 2);
    for ch in text.chars() {
        let mut units = [0u16; 2];
        for &unit in ch.encode_utf16(&mut units).iter() {
            for key_down in [1i32, 0i32] {
                records.push(InputRecordKeyEvent {
                    event_type: KEY_EVENT,
                    key_event: KeyEventRecord {
                        key_down,
                        repeat_count: 1,
                        virtual_key_code: 0,
                        virtual_scan_code: 0,
                        unicode_char: unit,
                        control_key_state: 0,
                    },
                });
            }
        }
    }
    let mut written: u32 = 0;
    // SAFETY: `handle` is caller-supplied per this function's own safety
    // contract; `records` is a valid buffer of exactly `records.len()`
    // initialized `InputRecordKeyEvent`s; `written` is a valid out-pointer.
    let ok =
        unsafe { WriteConsoleInputW(handle, records.as_ptr(), records.len() as u32, &mut written) };
    if ok == 0 {
        Err(Win32Error::last())
    } else {
        Ok(written)
    }
}

// Non-character virtual-key codes for [`write_key_events`] — arrows,
// Home/End, and friends carry no `uChar` at all, so [`write_char_events`]
// can't synthesize them; these are the ones a line editor's cursor/history
// navigation or keymap most plausibly needs to test.
pub const VK_PRIOR: u16 = 0x21;
pub const VK_NEXT: u16 = 0x22;
pub const VK_END: u16 = 0x23;
pub const VK_HOME: u16 = 0x24;
pub const VK_LEFT: u16 = 0x25;
pub const VK_UP: u16 = 0x26;
pub const VK_RIGHT: u16 = 0x27;
pub const VK_DOWN: u16 = 0x28;
pub const VK_INSERT: u16 = 0x2D;
pub const VK_DELETE: u16 = 0x2E;
pub const VK_F1: u16 = 0x70;
pub const VK_F2: u16 = 0x71;
pub const VK_F3: u16 = 0x72;
pub const VK_F4: u16 = 0x73;
pub const VK_F5: u16 = 0x74;
pub const VK_F6: u16 = 0x75;
pub const VK_F7: u16 = 0x76;
pub const VK_F8: u16 = 0x77;
pub const VK_F9: u16 = 0x78;
pub const VK_F10: u16 = 0x79;
pub const VK_F11: u16 = 0x7A;
pub const VK_F12: u16 = 0x7B;

/// `KEY_EVENT_RECORD.dwControlKeyState` bit a real keyboard driver always
/// sets for the navigation-cluster keys ([`VK_PRIOR`]/[`VK_NEXT`]/
/// [`VK_END`]/[`VK_HOME`]/[`VK_LEFT`]/[`VK_UP`]/[`VK_RIGHT`]/[`VK_DOWN`]/
/// [`VK_INSERT`]/[`VK_DELETE`]) — distinguishes them from their
/// numeric-keypad counterparts. [`write_key_events`] sets this
/// automatically for those keys rather than leaving a caller to remember
/// it, the one place this module deviates from its usual "thin,
/// policy-free wrapper" rule, since a real driver never *not* sets it for
/// these keys — there's no real policy choice here to leave to the caller.
pub const ENHANCED_KEY: u32 = 0x0100;

/// Synthesize one non-character key (arrows, Home/End, function keys, …) —
/// `WriteConsoleInputW`, the virtual-key-code counterpart to
/// [`write_char_events`]. `control_key_state` carries modifier bits (Shift/
/// Ctrl/Alt — not named as constants here, since Windows' own
/// `KEY_EVENT_RECORD.dwControlKeyState` values are already documented
/// bit-for-bit and this is a thin passthrough); pass `0` for an unmodified
/// keypress. [`ENHANCED_KEY`] is OR'd in automatically for the
/// navigation-cluster keys listed on its own doc — a caller doesn't need to
/// set it themselves.
///
/// The hardware scan code Windows pairs with `virtual_key_code` is looked
/// up via `MapVirtualKeyW` rather than left as `0` — the same "queue what a
/// real keyboard driver would" fidelity [`write_char_events`]'s own doc
/// describes, just extended to the one field that primitive never needed
/// (a character key's scan code is immaterial to VT translation the way a
/// non-character key's is).
///
/// # Safety
///
/// `handle` must be a currently-open, valid console input handle.
pub unsafe fn write_key_events(
    handle: RawHandle,
    virtual_key_code: u16,
    control_key_state: u32,
) -> Result<u32, Win32Error> {
    let is_enhanced = matches!(
        virtual_key_code,
        VK_PRIOR
            | VK_NEXT
            | VK_END
            | VK_HOME
            | VK_LEFT
            | VK_UP
            | VK_RIGHT
            | VK_DOWN
            | VK_INSERT
            | VK_DELETE
    );
    let control_key_state = control_key_state | if is_enhanced { ENHANCED_KEY } else { 0 };

    // SAFETY: `MAPVK_VK_TO_VSC` is the documented map-type constant for
    // this direction; `virtual_key_code` is a plain value, not a pointer.
    let scan_code = unsafe { MapVirtualKeyW(u32::from(virtual_key_code), MAPVK_VK_TO_VSC) } as u16;

    let records = [1i32, 0i32].map(|key_down| InputRecordKeyEvent {
        event_type: KEY_EVENT,
        key_event: KeyEventRecord {
            key_down,
            repeat_count: 1,
            virtual_key_code,
            virtual_scan_code: scan_code,
            unicode_char: 0,
            control_key_state,
        },
    });

    let mut written: u32 = 0;
    // SAFETY: `handle` is caller-supplied per this function's own safety
    // contract; `records` is a valid, fully-initialized 2-element buffer;
    // `written` is a valid out-pointer.
    let ok =
        unsafe { WriteConsoleInputW(handle, records.as_ptr(), records.len() as u32, &mut written) };
    if ok == 0 {
        Err(Win32Error::last())
    } else {
        Ok(written)
    }
}

